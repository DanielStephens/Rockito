package com.apakgroup.rockito.integration;

public class TestObject {

    private int i;

    private int k;

    private String str;

    private char c;

    public TestObject(final int i, final int k, final String str, final char c) {
        this.i = i;
        this.k = k;
        this.str = str;
        this.c = c;
    }

    public int getI() {
        return i;
    }

    public int getK() {
        return k;
    }

    public String getStr() {
        return str;
    }

    public char getC() {
        return c;
    }

}

